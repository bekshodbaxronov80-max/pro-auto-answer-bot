def get_text(lang: str, key: str, **kwargs) -> str:
    """Berilgan til va kalit bo'yicha matnni olish."""
    texts = TEXTS.get(lang, TEXTS["en"])
    text = texts.get(key, TEXTS["en"].get(key, f"Missing: {key}"))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    return text